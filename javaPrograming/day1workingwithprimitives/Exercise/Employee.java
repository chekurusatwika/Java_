package com.javaPrograming.day1workingwithprimitives.Exercise;

public class Employee {
    public static void main(String[] args) {
        byte age;
        age = 52;
        System.out.println("Age in the byte range : " + age);

        short empNo;
        empNo = 458;
        System.out.println("empNo in short range : " + empNo);

        int salary;
        salary = 20000;
        System.out.println("salary in int range : " + salary);

        long monthlySalary;
        monthlySalary = 250000000;
        System.out.println("Monthly Salary in long range: " + monthlySalary);

        float pf;
        pf = 5000.01f;
        System.out.println("Provident fund in float range: " + pf);

        double annualSalary;
        annualSalary = 500000000d;
        System.out.println("AnnualSalary in double range: " + annualSalary);

        char alphabet;
        alphabet = 'S';
        System.out.println("Alphabet is : " + alphabet);

        boolean isEligible;
        isEligible = true;
        System.out.println("Eligibility status : " + true);



    }
}
